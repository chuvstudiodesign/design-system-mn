import { blogAuthors } from "./authors";
import { blogCategories } from "./categories";
import type { BlogContentBlock, BlogPost } from "./types";

function tocFrom(content: BlogContentBlock[]) {
  return content
    .filter((block): block is Extract<BlogContentBlock, { type: "heading" }> => block.type === "heading")
    .map((block) => ({ id: block.id, title: block.text }));
}

function authorData(authorId: string) {
  const author = blogAuthors.find((item) => item.id === authorId) ?? blogAuthors[0];
  return {
    author: author.name,
    authorRole: author.role,
    authorAvatar: author.avatar,
  };
}

function categoryData(slug: string) {
  const category = blogCategories.find((item) => item.slug === slug) ?? blogCategories[0];
  return {
    category: category.name,
    categorySlug: category.slug,
  };
}

function post(input: Omit<BlogPost, "author" | "authorRole" | "authorAvatar" | "category" | "tableOfContents">): BlogPost {
  return {
    ...input,
    ...authorData(input.authorId),
    ...categoryData(input.categorySlug),
    tableOfContents: tocFrom(input.content),
  };
}

